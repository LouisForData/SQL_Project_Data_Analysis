SELECT
    job_title_short,
    job_location
FROM
    job_postings_fact;


/*

Label new column as follows:
- 'Anywhere' jobs as 'Remote'
- 'New york,NY 'jobs as 'Local'
- Otherwise 'Onsite'




*/
SELECT
    job_title_short,
    job_location,
    CASE
        WHEN job_location = 'Anywhere' THEN 'Remote'
        WHEN job_location = 'New York, NY' THEN 'Local'
        ELSE 'Onsite'
    END AS location_category
FROM
    job_postings_fact;


/*Number of jobs*/

SELECT
    COUNT(job_id) AS number_of_jobs,
    CASE
        WHEN job_location = 'Anywhere' THEN 'Remote'
        WHEN job_location = 'New York, NY' THEN 'Local'
        ELSE 'Onsite'
    END AS location_category
FROM
    job_postings_fact
GROUP BY  
 location_category 
;


